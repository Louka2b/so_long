#include "so_long.h"

void	ft_free_map(t_map **map, int i)
{
	int	j;

	j = 0;
	(void)i;
	while(j != (*map)->y)
	{
		free((*map)->map[j]);
		j++;
	}
	free((*map)->map);
	free((*map));
}
